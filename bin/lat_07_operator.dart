void main() {
  int a = 10, b = 3;
  double c = 10.0;

  print('\n$a + $b = ${a + b}');
  print('\n$a - $b = ${a - b}');
  print('\n$a * $b = ${a * b}');
  print('\n$c / $b = ${c / b}');
  print('\n$a ~/ $b = ${a ~/ b}');
  print('\n$a % $b = ${a % b}');

  print('Nilai a  = $a');
  print('++a = ${++a}');
  print('Nilai a akhir = $a');

  print('Nilai b  = $b');
  print('b++ = ${b++}');
  print('Nilai b akhir = $b');
}
